import { Resend } from "resend";

const resend = new Resend(process.env.RESEND_API_KEY);

export async function sendEmail(to, subject, html) {
  try {
    const data = await resend.emails.send({
      from: "Spottica <onboarding@resend.dev>", // puedes usar uno gen√©rico de Resend
      to,
      subject,
      html,
    });

    console.log("üìß Email enviado con Resend:", data);
  } catch (err) {
    console.error("‚ùå Error enviando email con Resend:", err);
    throw new Error("No se pudo enviar el correo con Resend");
  }
}
